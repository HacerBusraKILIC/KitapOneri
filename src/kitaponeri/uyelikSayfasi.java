    private void uyelikDevamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uyelikDevamActionPerformed
        // TODO add your handling code here:
        String userName = uyelikUserName.getText();
        String location = konumText.getText();
        String age = yasText.getText();
        String password = String.valueOf(sifreBelirle.getPassword());
        
        if (userName.length() == 0) // Checking for empty field
        {
            JOptionPane.showMessageDialog(null, "Kullanici adi bos birakilamaz.Lütfen konrol edip güncelleyiniz!");
        } else if (password.length() == 0) // Checking for empty field
        {
            JOptionPane.showMessageDialog(null, "Sifre bos birakilamaz.Lütfen konrol edip güncelleyiniz!");
        } else if(kontrolUserName(userName))
        {
            JOptionPane.showMessageDialog(null, "Bu kallanici adi kullanimda!");
        }
        
        try {
            if( (userName.length() != 0) && (password.length() != 0) && (kontrolUserName(userName) == false) ){
                uyeEkle(location, age, userName, password);
                uyelikDevamPage uyelikD = new uyelikDevamPage();
                uyelikD.setSize(700, 1000);
                uyelikD.setVisible(true);
                this.setVisible(false);
            }
        } catch (SQLException ex) {
            Logger.getLogger(uyelikSayfasi.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(uyelikSayfasi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_uyelikDevamActionPerformed

