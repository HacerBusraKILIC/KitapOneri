    private static void uyeEkle(String location, String age, String user_name, String password) throws SQLException {
        PreparedStatement prpstmt = null;

        prpstmt = con.prepareStatement(INSERT_QUERY_UYELIK);
        prpstmt.setString(1, location);
        prpstmt.setString(2, age);
        prpstmt.setString(3, user_name);
        prpstmt.setString(4, password);

        // sorguyu gönder
        prpstmt.executeUpdate();

        System.out.println("\n***" + user_name + "  kullanıcısı eklendi.");
    }

    private boolean kontrolUserName(String user_name) {
        PreparedStatement ps;
        ResultSet rs;
        boolean konrolUserName = false;
        String sqlKontrolKullaniciAdi = "SELECT * FROM `bx_users` WHERE `user_name` =?";
        
        try {
            ps = con.prepareStatement(sqlKontrolKullaniciAdi);
            ps.setString(1, user_name);
            rs = ps.executeQuery();
            
            if(rs.next())
            {
                konrolUserName = true;
            }
        } catch (SQLException ex) {
            System.out.println("kontrolUserName catch e düstü.");
            Logger.getLogger(uyelikSayfasi.class.getName()).log(Level.SEVERE, null, ex);
        }
         return konrolUserName;        
    }
