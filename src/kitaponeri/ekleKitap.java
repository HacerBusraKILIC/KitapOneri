    private static void kitapEkleDB(String isbn, String book_title, String book_author, String year_of_publication, String publisher, String image_url_s, String image_url_m, String image_url_l) throws SQLException {
        PreparedStatement prpstmt = null;

        prpstmt = con.prepareStatement(INSERT_QUERY_KITAP);
        prpstmt.setString(1, isbn);
        prpstmt.setString(2, book_title);
        prpstmt.setString(3, book_author);
        prpstmt.setString(4, year_of_publication);
        prpstmt.setString(5, publisher);
        prpstmt.setString(6, image_url_s);
        prpstmt.setString(7, image_url_m);
        prpstmt.setString(8, image_url_l);
        // sorguyu gönder
        prpstmt.executeUpdate();

        System.out.println("\n***" + isbn + " : "+book_title+ " kitabi eklendi.");
    }
    
    private boolean kontrolISBN(String isbn) {
        PreparedStatement ps;
        ResultSet rs;
        boolean konrolISBN = false;
        String sqlKontrolISBN = "SELECT * FROM `bx_books` WHERE `isbn` =?";
        
        try {
            ps = con.prepareStatement(sqlKontrolISBN);
            ps.setString(1, isbn);
            rs = ps.executeQuery();
            
            if(rs.next())
            {
                konrolISBN = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(uyelikSayfasi.class.getName()).log(Level.SEVERE, null, ex);
        }
         return konrolISBN;        
    }
    
