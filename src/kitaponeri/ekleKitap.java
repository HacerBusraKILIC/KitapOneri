    private void kitapEkleTamamlaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kitapEkleTamamlaActionPerformed
        String isbn = isbnText.getText();
        String kitapAdi = kitapAdiText.getText();
        String kitapYazari = kitapYazariText.getText();
        String yayinYili = yayinYiliText.getText();
        String yayinevi = yayineviText.getText();
        String urlS = urlSText.getText();
        String urlM = urlMText.getText();
        String urlL = urlLText.getText();
        
        try {
            if( (isbn.length() != 0) && (kitapAdi.length() != 0) && (kitapYazari.length() != 0) && (yayinYili.length() != 0)
                 && (yayinevi.length() != 0) && (urlS.length() != 0) && (urlM.length() != 0) && (urlL.length() != 0) ){
                 if(kontrolISBN(isbn) == false){
                     kitapEkleDB(isbn, kitapAdi, kitapYazari, yayinYili, yayinevi, urlS, urlM, urlL);
                     adminSayfasi admin = new adminSayfasi();
                     admin.tobloyuDoldur("bx_books");
                 }else{
                     JOptionPane.showMessageDialog(null, "Bu isbn koduna ait kitap daha önce eklenmiş.");
                 }
            }else{
                JOptionPane.showMessageDialog(null, "Lütfen bos alanlari kontrol ediniz.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ekleKitap.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_kitapEkleTamamlaActionPerformed

