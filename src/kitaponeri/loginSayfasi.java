    private boolean giris(String username, String password, String kullanici) {
        try {
            Class.forName("com.mysql.jdbc.Driver");  // MySQL database connection
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/kitapOneriKullanici?" + "user=root&password=1038");
            PreparedStatement pst = con.prepareStatement("Select * from bx_users where user_name=? and password=? and kullanici=?");
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3, kullanici);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                this.anlikKullaniciIsbn = rs.getString(1);
                return true;
            } else {
                return false;
            }
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }
    }

