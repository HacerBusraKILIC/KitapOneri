    private void GirisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GirisActionPerformed
        if (kullaniciAdi.getText().length() == 0) // Checking for empty field
        {
            JOptionPane.showMessageDialog(null, "Kullanici adi bos birakilamaz.Lütfen konrol edip güncelleyiniz!");
        } else if (kullaniciSifre.getPassword().length == 0) // Checking for empty field
        {
            JOptionPane.showMessageDialog(null, "Bos alanlari lütfen konrol edip güncelleyiniz!");
        } else {
            String userName = kullaniciAdi.getText();   // Collecting the input
            char[] pass = kullaniciSifre.getPassword(); // Collecting the input
            String pwd = String.copyValueOf(pass);  // converting from array to string
            if (giris(userName, pwd, "user")) {
                //JOptionPane.showMessageDialog(null, "Correct Login Credentials");
                anaSayfa anasayfa;
                try {
                    anasayfa = new anaSayfa();
                    anasayfa.setSize(900, 600);
                    anasayfa.setVisible(true);
                    this.setVisible(false);
                } catch (IOException ex) {
                    Logger.getLogger(loginSayfasi.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Incorrect Login Credentials");
            }
        }
    }//GEN-LAST:event_GirisActionPerformed

